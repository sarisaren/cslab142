/* write a c++ program to print all natural numbers from 1 to n using while loop */

#include <iostream>
using namespace std;

int main ()
{
//declare the variable
  int n;
  cout << "please enter the value" ;
//take the input
  cin >> n;
//give the condition
  while (n<=85) {
    cout << n << " , ";
    n++;
    }

  return 0;

}
